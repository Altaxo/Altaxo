    /// <summary>
    /// Gives the kth root of the Bessel function J0.
    /// </summary>
    /// <param name="k">The kth root (k &gt;=1).</param>
    /// <returns>The kth root of the Bessel function J0.</returns>
    /// <exception cref="System.ArgumentOutOfRangeException">k - k must be greater than 0.</exception>
    public static double BesselJ0Zero(int k)
    {
      if (!(k > 0))
      {
        throw new ArgumentOutOfRangeException(nameof(k), "k must be greater than 0.");
      }

      if (k <= _j0roots.Length)
      {
        return _j0roots[k - 1];
      }
      else
      {
        // use McMahon's formula, see https://dlmf.nist.gov/10.21#vi
        double a = (k - 0.25) * Math.PI;
        return a + 1 / (8 * a) - 31 / (384 * RMath.Pow3(a)) - 4176007 / (35840 * RMath.Pow5(a));
      }
    }

