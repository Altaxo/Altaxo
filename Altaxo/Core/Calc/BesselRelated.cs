          bj = 1.0;
          for (int j = m; j > 0; j--)
          {
            bjm = j * tox * bj - bjp;
            bjp = bj;
            bj = bjm;
            if (Math.Abs(bj) > bignum)
            {
              bj *= invbignum;
              bjp *= invbignum;
              ret_val *= invbignum;
              sum *= invbignum;
            }
            if (jsum)
              sum += bj;
            jsum = !jsum;
            if (j == n)
              ret_val = bjp;
          }
          sum = 2.0 * sum - bj;
          ret_val /= sum;
        }
        return (x < 0.0 && n % 2 == 1) ? -ret_val : ret_val;
      }
    }

