          double tox = 2.0 / ax;
          bjm = BesselJ0(ax, bDebug);
          bj = BesselJ1(ax, bDebug);
          for (int j = 1; j < n; j++)
          {
            bjp = j * tox * bj - bjm;
            bjm = bj;
            bj = bjp;
          }
          ret_val = bj;
        }
        else
        {
